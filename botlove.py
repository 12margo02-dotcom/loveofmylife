async def start(update: Update, context: ContextTypes.DEFAULT_TYPE):

    keyboard = [
    ["🫂 Обнимашка", "💜 Комплимент","📅 Мы знакомы","🌞 Сообщение дня", "😂 Анекдот"]
]

    reply_markup = ReplyKeyboardMarkup(
    keyboard,
    resize_keyboard=True
)

    await update.message.reply_text(
    "Привет, моя принцесса!!! В чем ты нуждаешься сейчас больше всего?",
    reply_markup=reply_markup
)
app.add_handler(CommandHandler("start", start))


async def buttons(update: Update, context: ContextTypes.DEFAULT_TYPE):

    text = update.message.text

    if text == "🫂 Обнимашка":
        await hug(update, context)

    elif text == "💜 Комплимент":
        await compliment(update, context)
    
    elif text == "📅 Мы знакомы":
        await days(update, context)

    elif text == "🌞 Сообщение дня":
        await daily_message(update, context)

    elif text == "😂 Анекдот":
        await joke(update, context)


app.add_handler(
    MessageHandler(filters.TEXT & ~filters.COMMAND, buttons)
)

from datetime import date

async def days(update, context):

    start_date = date(2026, 5, 8)

    today = date.today()

    days_together = (today - start_date).days

    await update.message.reply_text(
        f"Ты освещаешь мою жизнь уже {days_together} дней ❤️"
    )

